interface SearchFilters {
  keyword?: string
  useFlag?: string
  page?: number
  pageSize?: number
}

export interface SearchResult {
  items: CustomerListItem[]
  totalCount: number
}

export async function searchCustomers(
  dbConfig: DbConfig,
  filters: SearchFilters
): Promise<SearchResult> {
  const keyword = filters.keyword?.trim() || ''
  const useFlag = filters.useFlag || ''
  const page = filters.page || 1
  const pageSize = filters.pageSize || 20
  const offset = (page - 1) * pageSize

  const whereClause = `WHERE K01CUSGBCD IS NOT NULL
       AND (@keyword = '' OR K01CUSGBCD LIKE '%'+@keyword+'%' OR K01SANGHOX LIKE '%'+@keyword+'%' OR K01BOSS001 LIKE '%'+@keyword+'%' OR K01SAUJANO LIKE '%'+@keyword+'%')
       AND (@useFlag = '' OR ISNULL(K01DLTCODE,'N') = @useFlag)`

  const params = { keyword, useFlag, offset, pageSize }

  const [countRows, items] = await Promise.all([
    queryDynamic<{ cnt: number }>(
      dbConfig,
      `SELECT COUNT(*) AS cnt
       FROM TBK01 WITH (NOLOCK)
       LEFT JOIN T_STD_CDMST TC9 ON TC9.SubCode = K01GUBUN03 AND TC9.ClassCode = 'CUSTGUBUN'
       ${whereClause}`,
      { keyword, useFlag }
    ),
    queryDynamic<CustomerListItem>(
      dbConfig,
      `SELECT K01CUSGBCD, K01SANGHOX, K01SAUJANO, K01BOSS001, K01TELX001, K01FAXXNOX,
              K01ADDRES1, K01BOSS002, K01BOSS002_CEL,
              CASE K01GUBUN01 WHEN 'P' THEN '매입처' WHEN 'S' THEN '매출처' WHEN 'T' THEN '매입매출처' ELSE '' END AS K01GUBUN01_NM,
              ISNULL(TC9.CodeName, '') AS K01GUBUN03_NM,
              ISNULL(K01DLTCODE,'N') AS K01DLTCODE
       FROM TBK01 WITH (NOLOCK)
       LEFT JOIN T_STD_CDMST TC9 ON TC9.SubCode = K01GUBUN03 AND TC9.ClassCode = 'CUSTGUBUN'
       ${whereClause}
       ORDER BY K01CUSGBCD
       OFFSET @offset ROWS FETCH NEXT @pageSize ROWS ONLY`,
      params
    ),
  ])

  return { items, totalCount: countRows[0]?.cnt ?? 0 }
}

