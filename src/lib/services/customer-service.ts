export async function createCustomer(
  dbConfig: DbConfig,
  data: Record<string, unknown>,
  userId: string,
  plantCd: string
): Promise<{ custCd: string; warning?: string }> {
  // 사업자번호 중복 체크
  let warning: string | undefined
  const saujano = String(data.K01SAUJANO || '').replace(/-/g, '').trim()
  if (saujano) {
    const dupes = await queryDynamic<{ K01CUSGBCD: string; K01SANGHOX: string }>(
      dbConfig,
      `SELECT K01CUSGBCD, K01SANGHOX FROM TBK01 WITH (NOLOCK)
       WHERE REPLACE(K01SAUJANO,'-','') = @saujano`,
      { saujano }
    )
    if (dupes.length > 0) {
      warning = `동일 사업자번호 거래처 존재: ${dupes.map(d => `${d.K01CUSGBCD}(${d.K01SANGHOX})`).join(', ')}`
    }
  }

  // 자동채번 + INSERT
  await executeDynamic(
    dbConfig,
    `INSERT INTO TBK01 (
       K01KOREACD, K01CUSGBCD,
       K01SANGHOX, K01SANGHO2, K01SANGHOE, K01SAUJANO,
       K01GUBUN01, K01GUBUN03, K01DLTCODE, K01BOSS001,
       K01UPTEXXX, K01JONGMOK, K01ADDRES1, K01ADDRES2,
       K01POST001, K01TELX001, K01FAXXNOX, K01BOSS002,
       K01BOSS002_CEL, K01HOMEPG1, K01EMAIL01, K01EMAIL02,
       K01GUBUN04, K01GUBUN05, K01GUBUN06, K01JIOGXXX,
       K01PUMKCD, K01OPENDAY, K01TRATDAY, K01GULJECD,
       K01GULJEDY, K01KINDBAD, K01BANKCD, K01BANKACC,
       K01BANKHLD, K01VATMAN1, K01VATTEL1, K01VATSMS1,
       K01MANCD01, K01MANNM01, K01MANCD02, K01MANNM02,
       K01SHIPINFO, K01DELVTYPE, K01SHIP_ADDRES1, K01SHIP_ADDRES2,
       K01SHIP_POST001, K01SHIP_BOSS001, K01SHIP_BOSS001_CEL,
       K01DELVMON, K01DELVTUE, K01DELVWED, K01DELVTHU,
       K01DELVFRI, K01DELVSAT, K01DELVSUN,
       K01OWNERCD, K01OPTCHAR01, K01OPTCHAR02, K01OPTCHAR03,
       K01ETCCODE1, K01ETCCODE2, K01ETCCODE3, K01ETCCODE4, K01ETCCODE5,
       K01COMMISSION_RATE, K01REMARKS, K01BIGO101,
       K01SYSDATE, K01SYSUSER
     ) VALUES (
       @plantCd,
       (SELECT CONVERT(varchar, ISNULL(MAX(CONVERT(int, K01CUSGBCD)),10000)+1) FROM TBK01 WHERE ISNUMERIC(K01CUSGBCD)=1 AND LEN(K01CUSGBCD)<10),
       @K01SANGHOX, @K01SANGHO2, @K01SANGHOE, @K01SAUJANO,
       @K01GUBUN01, @K01GUBUN03, @K01DLTCODE, @K01BOSS001,
       @K01UPTEXXX, @K01JONGMOK, @K01ADDRES1, @K01ADDRES2,
       @K01POST001, @K01TELX001, @K01FAXXNOX, @K01BOSS002,
       @K01BOSS002_CEL, @K01HOMEPG1, @K01EMAIL01, @K01EMAIL02,
       @K01GUBUN04, @K01GUBUN05, @K01GUBUN06, @K01JIOGXXX,
       @K01PUMKCD,
       CASE WHEN @K01OPENDAY = '' THEN NULL ELSE @K01OPENDAY END,
       CASE WHEN @K01TRATDAY = '' THEN NULL ELSE @K01TRATDAY END,
       @K01GULJECD,
       CASE WHEN @K01GULJEDY = '' THEN NULL ELSE CONVERT(int, @K01GULJEDY) END,
       @K01KINDBAD, @K01BANKCD, @K01BANKACC,
       @K01BANKHLD, @K01VATMAN1, @K01VATTEL1, @K01VATSMS1,
       @K01MANCD01, @K01MANNM01, @K01MANCD02, @K01MANNM02,
       @K01SHIPINFO, @K01DELVTYPE, @K01SHIP_ADDRES1, @K01SHIP_ADDRES2,
       @K01SHIP_POST001, @K01SHIP_BOSS001, @K01SHIP_BOSS001_CEL,
       @K01DELVMON, @K01DELVTUE, @K01DELVWED, @K01DELVTHU,
       @K01DELVFRI, @K01DELVSAT, @K01DELVSUN,
       @K01OWNERCD, @K01OPTCHAR01, @K01OPTCHAR02, @K01OPTCHAR03,
       @K01ETCCODE1, @K01ETCCODE2, @K01ETCCODE3, @K01ETCCODE4, @K01ETCCODE5,
       CASE WHEN @K01COMMISSION_RATE = '' THEN NULL ELSE CONVERT(decimal(10,2), @K01COMMISSION_RATE) END,
       @K01REMARKS, @K01BIGO101,
       GETDATE(), @userId
     )`,
    { plantCd, userId, ...buildParams(data) }
  )

  // 채번된 코드 조회
  const result = await queryDynamic<{ NEWCD: string }>(
    dbConfig,
    `SELECT CONVERT(varchar, MAX(CONVERT(int, K01CUSGBCD))) AS NEWCD FROM TBK01 WHERE ISNUMERIC(K01CUSGBCD)=1 AND LEN(K01CUSGBCD)<10`
  )

  return { custCd: result[0]?.NEWCD || '', warning }
}

